package basisFx.appCore.chart;

public class NumberAxisValue {
    
    private Number xVal;
    private Number yYal;

    public NumberAxisValue(Number xVal, Number yYal) {
        this.xVal = xVal;
        this.yYal = yYal;
    }

    public Number getxVal() {
        return xVal;
    }

    public Number getyYal() {
        return yYal;
    }
}
